    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ExtensionFilter ef = new ExtensionFilter("*.pdf",".pdf");
        JFileChooser jfc = new JFileChooser(this._defaultDir);
        jfc.setAcceptAllFileFilterUsed(false);
        jfc.setFileFilter(ef);
        this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        if (JFileChooser.APPROVE_OPTION == jfc.showSaveDialog(this)) {
            File file = jfc.getSelectedFile();
            if (!file.getAbsolutePath().endsWith(".pdf")) {
                file = new File(file.getAbsolutePath() + ".pdf");
            }
            try {
                FileUtils.copy(_document, file);
                System.out.println("COPIADO");
            } catch (FileNotFoundException ex) {
                ErrorDialog.manejaError(ex);
            } catch (IOException ex) {
                ErrorDialog.manejaError(ex);
            }
        }
        this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_jButton2ActionPerformed

