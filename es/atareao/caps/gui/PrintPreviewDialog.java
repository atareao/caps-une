    public PrintPreviewDialog(java.awt.Frame parent,File document,File defaultDir) {
        super(parent, true);
        initComponents();
        Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();
        this.setSize(Convert.toInt(pantalla.width * 0.9), Convert.toInt(pantalla.height * 0.9));
        this.setLocationRelativeTo(null);
        this._document=document;
        this._defaultDir = defaultDir;
        try {
            RandomAccessFile raf = new RandomAccessFile(document, "r");
            FileChannel channel = raf.getChannel();
            ByteBuffer buf = channel.map(FileChannel.MapMode.READ_ONLY, 0, channel.size());
            this._pdf = new PDFFile(buf);
            this.jTotalPages.setText(" / "+Convert.toString(this.getHojas()));
            this.setPage(1);
        } catch (FileNotFoundException ex) {
            ErrorDialog.manejaError(ex);
        } catch (IOException ex) {
            ErrorDialog.manejaError(ex);
        }
    }
    private int getHojas(){
        return this._pdf.getNumPages();
    }
