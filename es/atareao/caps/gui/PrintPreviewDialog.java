    public int getPage() {
        return _page;
    }

    /**
     * @param page the _page to set
     */
    public void setPage(int page) {
        this._page = page;
        this.jPageNumber.setDouble(page);
        Image image=(new PdfPage(this._pdf,_page,100)).getImage();
        this.jImage1.setImage(image);
        int w=image.getWidth(this.jImage1);
        int h=image.getHeight(this.jImage1);
        Dimension d=new Dimension(w, h);
        this.jImage1.setMaximumSize(d);
        this.jImage1.setMaximumSize(d);
        this.jImage1.setPreferredSize(d);
        this.jImage1.setSize(d);
        this.jImage1.repaint();
    }

    /**
     * @return the _zoom
     */
    public int getZoom() {
        return _zoom;
    }

    /**
     * @param zoom the _zoom to set
     */
    public void setZoom(int zoom) {
        _zoom=zoom;
        double scale=zoom/100.0;
        this.jPageNumber.setDouble(_page);
        Image image=(new PdfPage(this._pdf,_page,zoom)).getImage();
        this.jImage1.setImage(image);
        int w=image.getWidth(this.jImage1);
        int h=image.getHeight(this.jImage1);
        Dimension d=new Dimension(Convert.toInt(w*scale), Convert.toInt(h*scale));
        System.out.println(scale);
        this.jImage1.setMaximumSize(d);
        this.jImage1.setMaximumSize(d);
        this.jImage1.setPreferredSize(d);
        this.jImage1.setSize(d);
        this.jImage1.setScaleWidth(scale);
        this.jImage1.setScaleHeight(scale);
        this.jImage1.repaint();
    }
